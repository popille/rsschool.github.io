<script>
document.addEventListener('DOMContentLoaded', function() {
    const axes = ['social', 'environnemental', 'economique', 'local', 'gouvernance'];
    let currentAxeIndex = 0;

    let scores = {};

    axes.forEach(axe => {
        scores[axe] = { oui: 0, non: 0, maxPossible: 0 };
    });

    scores.total = { oui: 0, non: 0, maxPossible: 0 };

    const axeContainer = document.getElementById('axe-container');
    const prevBtn = document.getElementById('prev-btn');
    const nextBtn = document.getElementById('next-btn');

    function scrollToTop() {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    }

    function showCurrentAxe() {
        document.querySelectorAll('.axe-section').forEach(section => {
            section.classList.remove('active');
        });
        document.querySelector(`#axe-${axes[currentAxeIndex]}`).classList.add('active');
        console.log(document.querySelector(`#axe-${axes[currentAxeIndex]}`));
        prevBtn.style.display = currentAxeIndex > 0 ? 'inline-block' : 'none';
        nextBtn.textContent = currentAxeIndex < axes.length - 1 ? 'Suivant' : 'Terminer';
        scrollToTop();
    }

    function updateScores() {
        // Réinitialiser les scores
        axes.forEach(axe => {
            scores[axe] = { oui: 0, non: 0, maxPossible: 0 };
        });
        scores.total = { oui: 0, non: 0, maxPossible: 0 };

        // Calculer les scores pour chaque axe
        axes.forEach(axe => {
            const axeElement = document.getElementById(`axe-${axe}`);
            if (axeElement) {
                const questions = axeElement.querySelectorAll('.card');
                questions.forEach((question, index) => {
                    const weight = index < 3 ? 4 : 1; // Les 3 premières questions ont un poids de 4
                    const oui = question.querySelectorAll('input[id$="-oui"]:checked').length;
                    const non = question.querySelectorAll('input[id$="-non"]:checked').length;
                    
                    scores[axe].oui += oui * weight;
                    scores[axe].non += non * weight;
                    scores[axe].maxPossible += question.querySelectorAll('input[type="radio"]').length * weight / 2; // Divisé par 2 car chaque question a deux options
                });

                scores.total.oui += scores[axe].oui;
                scores.total.non += scores[axe].non;
                scores.total.maxPossible += scores[axe].maxPossible;
            }
        });

        console.log('Scores actuels:', scores);
    }

    // Ajouter des écouteurs d'événements à tous les boutons radio
    document.querySelectorAll('input[type="radio"]').forEach(radio => {
        radio.addEventListener('change', updateScores);
    });

    prevBtn.addEventListener('click', () => {
        if (currentAxeIndex > 0) {
            currentAxeIndex--;
            showCurrentAxe();
        }
    });

    nextBtn.addEventListener('click', () => {
        updateScores();
        if (currentAxeIndex < axes.length - 1) {
            currentAxeIndex++;
            showCurrentAxe();
        } else {
            // Traitement final lorsque tous les axes ont été parcourus
            console.log('Questionnaire terminé');
            if(nextBtn.textContent == "Terminer") {
                axes.forEach(axe => {
                    scores[axe] = (scores[axe].oui / scores[axe].maxPossible) * 4;
                });
                scores.total = (scores.total.oui / scores.total.maxPossible) * 20;
                
                console.log('Scores finaux:', scores);
            }
        }
    });

    showCurrentAxe(); 
    updateScores(); // Calculer les scores initiaux
});
</script>